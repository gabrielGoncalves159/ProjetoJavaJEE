
public class Operacoes {

	public double calculoSoma(double num1, double num2) {
		double soma = num1 + num2;
		return soma;		
	}
	
	public double calculoSubtracao(double num1, double num2) {
		double sub = num1 - num2;
		return sub;
	}
	
	public double calculoMultiplicacao(double num1, double num2) {
		double mult = num1 * num2;
		return mult;
	}
	
	public double calculoDivisao(double num1, double num2) {
		double divi = num1 / num2;
		return divi;
	}
}
