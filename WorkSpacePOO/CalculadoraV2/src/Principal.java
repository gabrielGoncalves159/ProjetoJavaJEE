import javax.swing.JOptionPane;

public class Principal {

	public static void main(String[] args) {
		
		int continuar;
		double resultado =0;
		
		do {
			double num1 = EntradaSaida.solicitaNumero("1º");
			double num2 = EntradaSaida.solicitaNumero("2º");
			int operacao = EntradaSaida.solicitaOperacao();
			
			switch(operacao) {			
			case 1:
				resultado = calculoSoma(num1,num2);
				break;
			case 2:
				resultado = calculoSubtracao(num1,num2);
				break;
			case 3:
				resultado = calculoMultiplicacao(num1,num2);
				break;
			case 4:
				while(num2==0) {
					num2 = EntradaSaida.solicitaNumero("2º");
				}
				resultado = calculoDivisao(num1,num2);
				break;
			default:
				JOptionPane.showMessageDialog(null, "Operação inválida! A calculadora será encerrada!");
				System.exit(0);
			}
			
			EntradaSaida.mostraResultado(resultado, operacao);
			continuar = Integer.parseInt(JOptionPane.showInputDialog("Deseja continuar calculando? 1- Sim | 2- Não"));
			
		}while (continuar==1);

	}
	
	static double calculoSoma(double num1, double num2) {
		double soma = num1 + num2;
		return soma;		
	}
	
	static double calculoSubtracao(double num1, double num2) {
		double sub = num1 - num2;
		return sub;
	}
	
	static double calculoMultiplicacao(double num1, double num2) {
		double mult = num1 * num2;
		return mult;
	}
	
	static double calculoDivisao(double num1, double num2) {
		double divi = num1 / num2;
		return divi;
	}
	
}
